					if (!filter_var($email, FILTER_VALIDATE_EMAIL)) {
						$emailErr = "Please enter a valid email address.";
						$formErr = true;
					}
				}
				
				if (empty($_POST["contact-back"])) {
					$contBackErr = "Please let us know if we can contact you back.";
